    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
 if (databaru == true) { // prosess simpan atau edit
            try {
                String sql = "insert into project values('"+jTextField2.getText()+"','"+jTextField3.getText()+"','"+jTextField4.getText()+"')";
                java.sql.Connection conn = (java.sql.Connection)projectb.Employee.EmployeeDB();
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "successfully saved");
            } catch (SQLException | HeadlessException e) {
                JOptionPane.showMessageDialog(null, " ERROR");
            }
        } else {
            try {
                String sql = "update project SET p_name ='"+jTextField3.getText()+"',p_loc='"+jTextField4.getText()+"' where p_no='"+jTextField2.getText()+"'";
                java.sql.Connection conn = (java.sql.Connection)projectb.Employee.EmployeeDB();
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "successfully saved");
            } catch (SQLException | HeadlessException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        GetData();           // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

