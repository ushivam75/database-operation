    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
         databaru = false; // menampilkan data ke textboxt
        try {
            int row =jTable1.getSelectedRow();
            String tabel_klik=(jTable1.getModel().getValueAt(row, 0).toString());
            java.sql.Connection conn =(java.sql.Connection)projectb.Employee.EmployeeDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet sql = stm.executeQuery("select * from project where p_no='"+tabel_klik+"'");
            if(sql.next()){
                String id = sql.getString("p_no");
                jTextField2.setText(id);
                String des = sql.getString("p_name");
                jTextField3.setText(des);
                String uoh = sql.getString("p_loc");
                jTextField4.setText(uoh);
                
                              
            } 
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jTable1MouseClicked

