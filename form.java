    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
         databaru = false; // menampilkan data ke textboxt
        try {
            int row =jTable1.getSelectedRow();
            String tabel_klik=(jTable1.getModel().getValueAt(row, 0).toString());
            java.sql.Connection conn =(java.sql.Connection)projectb.Employee.EmployeeDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet sql = stm.executeQuery("select * from Employee where e_no='"+tabel_klik+"'");
            if(sql.next()){
                String id = sql.getString("e_no");
                jTextField1.setText(id);
                String des = sql.getString("e_name");
                jTextField2.setText(des);
                String uoh = sql.getString("Gender");
                jTextField3.setText(uoh);
                String item = sql.getString("Salary");
                jTextField4.setText(item);
                              
            } 
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jTable1MouseClicked

