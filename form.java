    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         if (databaru == true) { // prosess simpan atau edit
            try {
                String sql = "insert into Employee values('"+jTextField1.getText()+"','"+jTextField2.getText()+"','"+jTextField3.getText()+"','"+jTextField4.getText()+"')";
                java.sql.Connection conn = (java.sql.Connection)projectb.Employee.EmployeeDB();
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "successfully saved");
            } catch (SQLException | HeadlessException e) {
                JOptionPane.showMessageDialog(null, " ERROR");
            }
        } else {
            try {
                String sql = "update Employee SET e_name ='"+jTextField2.getText()+"',Gender='"+jTextField3.getText()+"',Salary='"+jTextField4.getText()+"' where e_no='"+jTextField1.getText()+"'";
                java.sql.Connection conn = (java.sql.Connection)projectb.Employee.EmployeeDB();
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "successfully saved");
            } catch (SQLException | HeadlessException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        GetData();    
    }//GEN-LAST:event_jButton2ActionPerformed

